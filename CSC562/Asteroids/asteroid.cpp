#include <GL/gl.h>
#include <GL/glu.h>
#include <GL/glut.h>


void drawPole(float pole[3], float points[][3]){
	for( int i = 0; i < 8; i++){
		glBegin( GL_TRIANGLES );
    	glColor3f(1, 0, 0);
    	glVertex3fv(points[i]);
    	glVertex3fv(points[(i+1)%8]);
    	glVertex3fv(pole);
    	glEnd();
   	}
}

